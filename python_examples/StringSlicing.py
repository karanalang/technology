str = 'python'

print(" first 3 characters n string -> ", str[:3:1])
print(" first 4 characters n string, skip 2 -> ", str[::2])

print(" last character iof string -> ", str[-1])
print(" 2nd last character of string -> ", str[-2])
print("reversed string -> ", str[::-1])
print("reversed string, skip 2 -> ", str[::-2])
print("last 4 characters string -> ", str[-4:])

print(" complete string -> str[0:len(str)] ", str[0:len(str)])
print(" using str[-1: ] ", str[-1:])

